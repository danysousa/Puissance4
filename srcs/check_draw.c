#include "alcu.h"

int			check_draw(t_env *e)
{
	int		tmp;

	tmp = 0;
	while (tmp < e->dim_x)
	{
		if (e->board[0][tmp] == '.')
			return (0);
		tmp++;
	}
	e->player = 3;
	return (1);
}
