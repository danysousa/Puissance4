#include "alcu.h"

void		ft_putnbr(int n)
{
	if (n < 0)
	{
		ft_putchar('-');
		n = 0 - n;
	}
	if (n > 9)
		ft_putnbr(n / 10);
	ft_putchar((n % 10) + 48);
}
